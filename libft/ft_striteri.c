void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	idx;

	if (!s || !f)
		return ;
	idx = 0;
	while (*s)
	{
		f(idx, s);
		idx++;
		s++;
	}
	*s = '\0';
}
