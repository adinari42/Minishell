#include "libft.h"
/*returns 1 if is in ascii range,0 if not*/
int	ft_isalpha(int c)
{
	if (c >= 65 && c <= 90)
		return (1);
	if (c >= 97 && c <= 122)
		return (2);
	else
		return (0);
}
